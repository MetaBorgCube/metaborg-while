package ds.manual.interpreter;

public class Natives {

	public static int int_times_2(int v1, int v2) {
		return v1 * v2;
	}

	public static int int_plus_2(int v1, int v2) {
		return v1 + v2;
	}

	public static int int_minus_2(int v1, int v2) {
		return v1 - v2;
	}

	public static boolean int_gt_2(int v1, int v2) {
		return v1 > v2;
	}

}
